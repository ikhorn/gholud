#define USRC_TYPE_ITEM_STR								DSP_STR_SPACE_PUTTED	//тип строки пункта не выбранного
#define USRC_TYPE_ACTITEM_STR							DSP_STR_SPACE_PUTTED	//тип активной строки пункта (текущей)

//------------------------------------------------------------------------------
//работа с сообщениями
//-`-----------------------------------------------------------------------------

#define USRC_USE_ADD_MSG_SIGNAL							0						//использовать дополнительный сигнал сообщения
