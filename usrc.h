//------------------------------------------------------------------------------
//использование памяти
//------------------------------------------------------------------------------

#define USRC_RETURN_STACK_SIZE							20						//размер стека возвращения из меню
//#define usrcs_										flash_					//спецификатор памяти расположения строк
#define usrcw_											flash_
//спецификатор памяти расположения окон
