#define USRC_USE_WRT_SUCH_HOTKEY_3						'7'
#define USRC_USE_WRT_SUCH_HOTKEY_4						'9'
#define USRC_USE_WRT_SUCH_HOTKEY_5						0

//------------------------------------------------------------------------------
//клвиши навигации
//------------------------------------------------------------------------------

#define USRC_USE_ENTERKEY_2								1						//использование альтернативной клавиши входа, 0 - нет, 1 - да
#define USRC_USE_MINUSKEY								1						//использовать клавишу ввода минуса
//#define USRC_USE_TOGGLEMENU_FUNC						1						//использовать функцию, при быстром входе и выходе из меню

#define USRC_UPKEY										'2'						//клавиша вверх
#define USRC_DOWNKEY									'8'						//клавиша вниз
#define USRC_LEFTKEY									'4'						//клавиша влево
#define USRC_RIGHTKEY									'6'						//клавиша вправо
#define USRC_ENTERKEY_1									'='						//клавиша входа и ввода
#define USRC_ENTERKEY_2									'5'						//альтернативная клавиша входа
#define USRC_RETURNKEY									'b'						//клавиша возвращения или стирания
#define USRC_TOGGLEMENU_KEY								'c'						//клавиша быстрого входа и выхода из меню
#define USRC_MINUSKEY									'.'						//клавиша '-'

//------------------------------------------------------------------------------
//текстовый редактор
//------------------------------------------------------------------------------

#define USRC_USE_TEXT_EDITOR							1						//использование текстового редактора
		#define ___USRC_EXT				1										//внешний буфер
		#define ___USRC_LARGE_RAM		2										//большой буфер (больше длины строки)
		#define ___USRC_SMALL_RAM		3										//маленький буфер (меньше равно длине строки)
	#define USRC_TEXT_BUF_TYPE							___USRC_LARGE_RAM		//тип буфера для ввода текста

	#define ___USRC_UKR					1
	#define ___USRC_RUS					2
#define USRC_LANGUAGE									___USRC_RUS				//основной язык

//------------------------------------------------------------------------------
//работа с датой и временем
//------------------------------------------------------------------------------

#define USRC_USE_FULL_DATE				1										//использовать полную дату

#define USRC_DEF_D						'2','0'									//дата по умолчанию
#define USRC_DEF_M						'1','2'
