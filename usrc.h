//------------------------------------------------------------------------------
//работа со строками
//------------------------------------------------------------------------------

	#define ___USRC_2_STR				2
	#define ___USRC_4_STR				4
	#define ___USRC_6_STR				6
	#define ___USRC_8_STR				8
